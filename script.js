function حفظالنتيجة(اسمالاختبار, النتيجة) {
    localStorage.setItem(اسمالاختبار, النتيجة);
}

function عرضالنتيجة(اسمالاختبار) {
    return localStorage.getItem(اسمالاختبار);
}
